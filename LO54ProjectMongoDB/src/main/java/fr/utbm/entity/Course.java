@Entity
public class Course implements Serializable {
    
    @Id
    private String code;
    
    @Column
    private String title;
        
    public Course() {
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

}
